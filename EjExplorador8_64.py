#Fibonacci

def fibonacci(n):
    fib = [0, 1]
    while len(fib) < n:
        fib.append(fib[-1] + fib[-2])
    return fib

cantidad_elementos = int(input("Ingrese el nÃºmero de elementos de la serie Fibonacci: "))
resultado = fibonacci(cantidad_elementos)
print(f"Los primeros {cantidad_elementos} elementos de la serie Fibonacci son: {resultado}")